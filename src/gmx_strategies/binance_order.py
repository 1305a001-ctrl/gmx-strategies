async def place_market_order(
    symbol: str,
    side: str,
    notional_usd: float,
    *,
    mark_price: float,
    reduce_only: bool = False,
    client_order_id: str | None = None,
    dry_run: bool = True,
    client: httpx.AsyncClient | None = None,
) -> OrderResult:
    """Place (or dry-run) a MARKET order on Binance USDT-M Futures.

    Pre-flight (all checked before ANY HTTP call):
      1. side ∈ ALLOWED_SIDES
      2. symbol ∈ ALLOWED_SYMBOLS
      3. Symbol present in cached exchangeInfo (`get_cached_exchange_info`)
      4. Compute quantity via `quantity_from_notional(info, notional_usd,
         mark_price)`. Zero → below-lot-min rejection.
      5. `passes_min_notional(info, qty, mark_price)` → False = exchange
         min-notional rejection (e.g. BTC at $5 cap when min is $50).
      6. Generate `client_order_id` if not supplied (audit §12 idempotency).

    On `dry_run=True` (the default):
      Returns `OrderResult(submitted=False, dry_run_request=<params>)`.
      The dict is what WOULD have been signed and POSTed — operators
      can inspect it without anything reaching Binance.

    On `dry_run=False`:
      GATE 1: `settings.live_binance_enabled` MUST be True.
              False → `gate_blocked="live_binance_enabled=False"`,
              no broadcast.
      GATE 2: `assert_one_way_position_mode()` MUST succeed.
              Hedge or unknown → `gate_blocked="hedge_mode_or_api_down"`,
              no broadcast.
      Then issues `signed_post("/fapi/v1/order", ...)`. The response is
      parsed into `OrderResult` — `submitted=True` on the happy path,
      `error_code`/`error_msg` populated on a Binance reject.

    Args:
        symbol: Must be one of ALLOWED_SYMBOLS (whitelist enforced).
        side: Must be one of ALLOWED_SIDES.
        notional_usd: Target USD notional. Snap-rounded to lot_step via
            `quantity_from_notional`.
        mark_price: Current mark price for the symbol (caller's choice
            of price source — typically `binance_funding` markPrice or
            chainlink-streams benchmark). Used for sizing + min_notional
            check ONLY; the actual fill price is whatever Binance returns.
        reduce_only: When True, set `reduceOnly=true`. Use on CLOSE orders.
        client_order_id: Optional pre-supplied id (caller-controlled
            retry). When None, one is generated.
        dry_run: When True (default), do not broadcast. Even with
            dry_run=False, the gate-stack must clear.
        client: Optional shared httpx.AsyncClient.

    Returns:
        OrderResult — never raises.
    """
    # ── Pre-flight 1: side whitelist ─────────────────────────────────
    if side not in ALLOWED_SIDES:
        log.warning("binance_order.bad_side side=%s", side)
        return _make_local_error(
            error_msg=f"side not allowed: {side!r} (allowed: {sorted(ALLOWED_SIDES)})",
            client_order_id=client_order_id,
        )

    # ── Pre-flight 2: symbol whitelist ──────────────────────────────
    if symbol not in ALLOWED_SYMBOLS:
        log.warning("binance_order.bad_symbol symbol=%s", symbol)
        return _make_local_error(
            error_msg=(
                f"symbol not in whitelist: {symbol!r} "
                f"(allowed: {sorted(ALLOWED_SYMBOLS)})"
            ),
            client_order_id=client_order_id,
        )

    # ── Pre-flight 3: exchangeInfo for symbol ───────────────────────
    info_map = await binance_exchange_info.get_cached_exchange_info(client=client)
    info = info_map.get(symbol)
    if info is None:
        log.warning(
            "binance_order.symbol_missing_in_exchange_info symbol=%s "
            "n_known=%d", symbol, len(info_map),
        )
        return _make_local_error(
            error_msg=f"symbol not in exchange_info: {symbol!r}",
            client_order_id=client_order_id,
        )

    # ── Pre-flight 4: compute quantity from notional ────────────────
    qty = binance_exchange_info.quantity_from_notional(
        info, notional_usd, mark_price,
    )
    if qty <= 0:
        log.warning(
            "binance_order.qty_below_lot_min symbol=%s notional_usd=%.4f "
            "mark_price=%.4f lot_step=%s lot_min=%s",
            symbol, notional_usd, mark_price, info.lot_step, info.lot_min,
        )
        return _make_local_error(
            error_msg=(
                f"below_lot_min symbol={symbol} notional_usd={notional_usd} "
                f"mark_price={mark_price} lot_step={info.lot_step} "
                f"lot_min={info.lot_min}"
            ),
            client_order_id=client_order_id,
        )

    # ── Pre-flight 5: min_notional (after rounding!) ────────────────
    if not binance_exchange_info.passes_min_notional(info, qty, mark_price):
        log.warning(
            "binance_order.below_min_notional symbol=%s qty=%s "
            "mark_price=%.4f min_notional=%s",
            symbol, qty, mark_price, info.min_notional,
        )
        return _make_local_error(
            error_msg=(
                f"below_min_notional symbol={symbol} qty={qty} "
                f"mark_price={mark_price} min_notional={info.min_notional}"
            ),
            client_order_id=client_order_id,
        )

    # ── Pre-flight 6: generate client_order_id if absent ────────────
    if client_order_id is None:
        client_order_id = _generate_client_order_id()

    # ── Assemble the request ────────────────────────────────────────
    req = OrderRequest(
        symbol=symbol,
        side=side,
        type="MARKET",
        quantity=qty,
        reduce_only=reduce_only,
        client_order_id=client_order_id,
    )
    params = req.to_signed_params()

    # ── Dry-run path: do NOT call Binance ───────────────────────────
    if dry_run:
        log.info(
            "binance_order.dry_run symbol=%s side=%s qty=%s "
            "client_order_id=%s reduce_only=%s",
            symbol, side, qty, client_order_id, reduce_only,
        )
        return OrderResult(
            submitted=False,
            dry_run_request=params,
            order_id=None,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=None,
            error_msg=None,
            gate_blocked=None,
            error_slug=None,
        )

    # ── Live path: GATE 1 — global kill switch ──────────────────────
    if not settings.live_binance_enabled:
        log.warning(
            "binance_order.gate live=disabled symbol=%s side=%s qty=%s "
            "(settings.live_binance_enabled=False)",
            symbol, side, qty,
        )
        return OrderResult(
            submitted=False,
            dry_run_request=params,
            order_id=None,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=None,
            error_msg=None,
            gate_blocked="live_binance_enabled=False",
            error_slug=None,
        )

    # ── Live path: GATE 2 — position-mode (audit H3) ────────────────
    try:
        await binance_startup_check.assert_one_way_position_mode()
    except RuntimeError as exc:
        log.warning(
            "binance_order.gate position_mode=blocked symbol=%s err=%s",
            symbol, exc,
        )
        return OrderResult(
            submitted=False,
            dry_run_request=params,
            order_id=None,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=None,
            error_msg=str(exc),
            gate_blocked="hedge_mode_or_api_down",
            error_slug=None,
        )

    # ── Live path: broadcast ────────────────────────────────────────
    log.info(
        "binance_order.broadcast symbol=%s side=%s qty=%s "
        "client_order_id=%s reduce_only=%s",
        symbol, side, qty, client_order_id, reduce_only,
    )
    body = await signed_post("/fapi/v1/order", params, client=client)
    if not isinstance(body, dict):
        # `signed_post` returned None (HTTP error) or a list (impossible
        # for /fapi/v1/order but defensive). Surface as a transport
        # failure — we don't know order state. Caller can reconcile via
        # `get_order_status(client_order_id=...)`.
        log.error(
            "binance_order.broadcast_no_body symbol=%s client_order_id=%s",
            symbol, client_order_id,
        )
        return OrderResult(
            submitted=False,
            dry_run_request=params,
            order_id=None,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=None,
            error_msg="no_response_body_from_signed_post",
            gate_blocked=None,
            error_slug=None,
        )

    result = _parse_binance_response(body, client_order_id=client_order_id)
    if result.submitted:
        log.info(
            "binance_order.broadcast_ok symbol=%s order_id=%s status=%s "
            "executed_qty=%s avg_price=%s cum_quote=%s",
            symbol, result.order_id, result.status, result.executed_qty,
            result.avg_price, result.cum_quote,
        )
    else:
        log.warning(
            "binance_order.broadcast_reject symbol=%s code=%s slug=%s msg=%s",
            symbol, result.error_code, result.error_slug, result.error_msg,
        )
    return result


async def get_order_status(
    symbol: str,
    *,
    order_id: int | None = None,
    client_order_id: str | None = None,
    client: httpx.AsyncClient | None = None,
) -> OrderResult | None:
    """Reconcile an order's state via `GET /fapi/v1/order`.

    Per audit §12: on any network-ambiguous failure (request sent,
    response truncated, timeout), call this with the SAME `client_order_id`
    we generated for the submit. If the order exists, Binance returns
    its canonical state. If it does not, Binance returns `-2013 Order
    does not exist.` — the caller can safely retry with the same id.

    Args:
        symbol: Must be one of ALLOWED_SYMBOLS (whitelist enforced even
            for reads, to prevent typo-driven queries of unrelated
            symbols).
        order_id: Binance's numeric orderId (from a previous OrderResult).
        client_order_id: Caller-supplied idempotency id. Either this OR
            order_id must be set; if both are set Binance uses orderId
            and ignores origClientOrderId.
        client: Optional shared httpx.AsyncClient.

    Returns:
        OrderResult with the current state (`submitted=True` if the
        order exists in any state — including CANCELED / EXPIRED, the
        existence is the contract), or an OrderResult with `error_code`
        / `error_msg` on a Binance error. Returns None ONLY if the
        symbol is outside the whitelist (caller bug).
    """
    if symbol not in ALLOWED_SYMBOLS:
        log.warning("binance_order.get_order_status.bad_symbol symbol=%s", symbol)
        return None
    if order_id is None and not client_order_id:
        log.warning(
            "binance_order.get_order_status.no_id symbol=%s — "
            "must supply order_id or client_order_id", symbol,
        )
        return None

    params: dict[str, str | int | float] = {"symbol": symbol}
    if order_id is not None:
        params["orderId"] = order_id
    elif client_order_id:
        params["origClientOrderId"] = client_order_id

    body = await signed_get("/fapi/v1/order", params, client=client)
    if not isinstance(body, dict):
        log.warning(
            "binance_order.get_order_status.no_body symbol=%s "
            "order_id=%s client_order_id=%s",
            symbol, order_id, client_order_id,
        )
        return OrderResult(
            submitted=False,
            dry_run_request=None,
            order_id=order_id,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=None,
            error_msg="no_response_body_from_signed_get",
            gate_blocked=None,
            error_slug=None,
        )

    # The /fapi/v1/order GET response has the same shape as the POST
    # response in the happy path, so we reuse the parser.
    return _parse_binance_response(body, client_order_id=client_order_id)


async def cancel_order(
    symbol: str,
    *,
    order_id: int | None = None,
    client_order_id: str | None = None,
    client: httpx.AsyncClient | None = None,
) -> dict[str, Any] | None:
    """Cancel an open order via `DELETE /fapi/v1/order`.

    For MARKET orders this is rarely useful — they fill immediately. The
    helper is here for LIMIT iterations (G6.5+) and for cleanup paths in
    test scaffolding.

    Args:
        symbol: One of ALLOWED_SYMBOLS.
        order_id: OR client_order_id — at least one required.
        client_order_id: idempotency id from a prior submit.
        client: Optional shared httpx.AsyncClient.

    Returns:
        Binance's response body dict on success (includes the canceled
        order's final state), None on whitelist / id-missing failure or
        any transport / parse error.
    """
    if symbol not in ALLOWED_SYMBOLS:
        log.warning("binance_order.cancel_order.bad_symbol symbol=%s", symbol)
        return None
    if order_id is None and not client_order_id:
        log.warning(
            "binance_order.cancel_order.no_id symbol=%s — "
            "must supply order_id or client_order_id", symbol,
        )
        return None

    params: dict[str, str | int | float] = {"symbol": symbol}
    if order_id is not None:
        params["orderId"] = order_id
    elif client_order_id:
        params["origClientOrderId"] = client_order_id

    body = await signed_delete("/fapi/v1/order", params, client=client)
    if not isinstance(body, dict):
        log.warning(
            "binance_order.cancel_order.no_body symbol=%s "
            "order_id=%s client_order_id=%s",
            symbol, order_id, client_order_id,
        )
        return None
    return body


