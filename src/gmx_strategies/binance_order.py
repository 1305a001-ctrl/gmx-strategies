def _generate_client_order_id() -> str:
    """Build a `newClientOrderId` matching Binance's regex.

    Format: `<settings.binance_order_idempotency_prefix><uuid4_hex[:16]>`.
    Default prefix `gmx-strategies-` + 16 hex chars = 31 chars total
    (Binance cap is 36). The hex is a 64-bit slice of a uuid4 — collisions
    are not a practical concern at our 10-orders-per-day cadence.

    The regex Binance enforces is `^[\\.A-Z\\:/a-z0-9_-]{1,36}$`. The
    default prefix uses only `[a-z0-9-]` so we stay well within bounds.
    Operators overriding `binance_order_idempotency_prefix` MUST keep the
    prefix to the allowed character class.
    """
    return f"{settings.binance_order_idempotency_prefix}{uuid.uuid4().hex[:16]}"


def _make_local_error(
    *,
    error_msg: str,
    error_code: int = -1,
    client_order_id: str | None = None,
    dry_run_request: dict[str, str | int | float] | None = None,
) -> OrderResult:
    """Build an OrderResult that represents a local pre-flight rejection.

    Local errors use `error_code=-1` as a sentinel (different from any
    real Binance code, all of which are 4-digit negatives). `error_slug`
    is left None — slugs are reserved for known Binance codes.
    """
    return OrderResult(
        submitted=False,
        dry_run_request=dry_run_request,
        order_id=None,
        client_order_id=client_order_id,
        status=None,
        executed_qty=None,
        avg_price=None,
        cum_quote=None,
        error_code=error_code,
        error_msg=error_msg,
        gate_blocked=None,
        error_slug=None,
    )


def _parse_binance_response(
    body: dict[str, Any],
    *,
    client_order_id: str | None,
) -> OrderResult:
    """Parse a Binance order response into an OrderResult.

    Binance returns one of two shapes for /fapi/v1/order POST:

      Happy (newOrderRespType=RESULT):
        {
          "orderId": 12345,
          "clientOrderId": "gmx-strategies-...",
          "symbol": "SOLUSDT",
          "status": "FILLED",
          "executedQty": "0.034",
          "cumQuote": "5.12",
          "avgPrice": "150.58",
          "side": "BUY",
          "type": "MARKET",
          ... // other fields we ignore
        }

      Error (e.g. -4164, -1111, -2019, -2010, -4061):
        {
          "code": -4164,
          "msg": "Order's notional must be no smaller than 5.0."
        }

    `code` being present (and an int) is the error sentinel — even if
    `status` were also somehow present, the canonical error shape uses
    `{code, msg}` per the audit + Binance error-code docs.

    On a happy response missing some optional numeric field (e.g. older
    Binance behavior where ACK was returned by accident), we surface
    what we got and leave the missing fields as None — caller can still
    use `submitted=True` + `order_id` to reconcile via `get_order_status`.
    """
    code = body.get("code")
    msg = body.get("msg")
    if isinstance(code, int):
        # Error path
        err_msg = msg if isinstance(msg, str) else "unknown_error"
        return OrderResult(
            submitted=False,
            dry_run_request=None,
            order_id=None,
            client_order_id=client_order_id,
            status=None,
            executed_qty=None,
            avg_price=None,
            cum_quote=None,
            error_code=code,
            error_msg=err_msg,
            gate_blocked=None,
            error_slug=_classify_error(code),
        )

    # Happy path — best-effort numeric coercion (Binance returns strings)
    order_id_raw = body.get("orderId")
    order_id = order_id_raw if isinstance(order_id_raw, int) else None
    status_raw = body.get("status")
    status = status_raw if isinstance(status_raw, str) else None
    actual_client_order_id = body.get("clientOrderId")
    if isinstance(actual_client_order_id, str) and actual_client_order_id:
        client_order_id = actual_client_order_id

    def _to_float(raw: Any) -> float | None:
        if not isinstance(raw, (str, int, float)):
            return None
        try:
            value = float(raw)
        except (ValueError, TypeError):
            return None
        # NaN / inf guard
        if value != value or value in (float("inf"), float("-inf")):
            return None
        return value

    return OrderResult(
        submitted=True,
        dry_run_request=None,
        order_id=order_id,
        client_order_id=client_order_id,
        status=status,
        executed_qty=_to_float(body.get("executedQty")),
        avg_price=_to_float(body.get("avgPrice")),
        cum_quote=_to_float(body.get("cumQuote")),
        error_code=None,
        error_msg=None,
        gate_blocked=None,
        error_slug=None,
    )


