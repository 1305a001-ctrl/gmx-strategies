"""Binance USDT-M Futures order placement (G6.4 — CEX hedge leg).

The actual order-placement surface for the CEX hedge leg of the
funding-arb. Sits on top of:
  - G6.1 `binance_exchange_info.py` — symbol filter cache; we MUST snap-
    round quantity through `quantity_from_notional` + `round_qty_down` and
    final-gate it with `passes_min_notional` BEFORE submission, or every
    order will reject `-4164` (MIN_NOTIONAL) or `-1111` (PRECISION).
  - G6.2 `binance_auth.py` — `signed_post` / `signed_get` / (new in G6.4)
    `signed_delete` for the HMAC plumbing.
  - G6.2 `binance_startup_check.py` — `assert_one_way_position_mode`
    refuses to run in hedge mode (audit H3). G6.4 invokes this as the
    last gate before broadcasting a real order; failing means the
    operator hasn't flipped the account back to one-way via the UI.
  - G5.2 `settings.live_binance_enabled` — global kill-switch. Defaults
    False. Mirrors the gate-stack pattern from `gmx_signer.submit_signed`.

WHY THIS EXISTS — short version:
  G6.1-G6.3 shipped the READ surface (filters + auth + smoke). G6.4 is
  the WRITE surface. This is where a real order can leave the box and
  reach Binance. Every safety belt above is here.

GATE STACK (`place_market_order`):

    | dry_run | live_binance_enabled | position-mode | Broadcast? |
    |---------|----------------------|---------------|------------|
    | True    | any                  | any           | NO (sim)   |
    | False   | False                | any           | NO (gate)  |
    | False   | True                 | HEDGE / unkn  | NO (gate)  |
    | False   | True                 | one-way       | YES        |

  NO real broadcast unless ALL THREE are simultaneously true:
    dry_run=False AND live_binance_enabled=True AND position-mode is one-way.
  Mirrors the GMX side's `submit_signed` gate matrix exactly so an
  operator who knows one knows the other.

WHAT THIS IS NOT (deferred to G7.x):
  - NO consumption of `funding_arb:signals`. The runtime wiring is G7.1.
  - NO position reconciliation loop. That's G7.x — for now G6.4 ships
    the per-call helpers and the consumer composes them.
  - NO TWAP / spread / limit-orders. MARKET-only this PR; LIMIT is a
    later iteration once the basic surface is proven.
  - NO retry logic on partial fills or post-broadcast timeouts. The
    audit §12 reconciliation pattern (query-via-clientOrderId) is
    implemented as a discoverable helper (`get_order_status`) but the
    caller decides retry semantics.

SYMBOL / SIDE / TYPE WHITELIST:
  Per the audit §H1, only the 5 funding-arb basket symbols are allowed.
  Sides: BUY (open long / close short) and SELL (open short / close
  long). Types: MARKET only — LIMIT-IOC was scoped out for G6.4 because
  the funding-arb hedge leg is always taker (matches GMX's market-fill
  on the other side).

IDEMPOTENCY (newClientOrderId):
  Every order generated by `place_market_order` carries a
  `newClientOrderId` of the form:
      <settings.binance_order_idempotency_prefix><uuid4_hex[:16]>
  (default `gmx-strategies-` + 16 hex = 31 chars; fits Binance's 36-cap
  regex `^[\\.A-Z\\:/a-z0-9_-]{1,36}$`).

  Callers can pre-generate and pass `client_order_id` for deterministic
  retry — re-issuing the SAME id is the audit §12 reconciliation pattern:
  if the order already exists, Binance returns the existing state;
  otherwise the new order is placed. Either way the caller gets a
  canonical state from a single round-trip on retry.

SECRETS HYGIENE:
  NEVER log the api_key/secret/signed query. Same rules as binance_auth.
  We log: symbol, side, type, quantity, status, error_code, error_msg.

  We log REQUEST params (the dict that would be signed) on dry_run
  because that's the operator's only visibility into what would be sent
  — and the dict does NOT contain the signature or api_key. The signed
  payload (signature, X-MBX-APIKEY) is built downstream in
  `binance_auth._signed_request` and never crosses back into this
  module.

ERROR CODE MAPPING:
  Audit §12 catalogs the codes we care about. We map each known code to
  a stable human-readable message string on `OrderResult.error_msg`:

    -1013 INVALID_MESSAGE         "invalid_message_or_lot_size"
    -1111 BAD_PRECISION           "precision_mismatch"
    -2010 NEW_ORDER_REJECTED      "new_order_rejected"
    -2019 MARGIN_NOT_SUFFICIENT   "margin_not_sufficient"
    -4061 POSITION_SIDE_NOT_MATCH "position_side_not_match_hedge_mode"
    -4164 MIN_NOTIONAL            "below_min_notional_exchange"

  Unknown codes are surfaced verbatim (`OrderResult.error_code` carries
  the raw int; `error_msg` carries the raw Binance message string).
"""

