# The 5 funding-arb basket symbols. Refusing anything outside this set —
# even if `exchangeInfo` has it — is the audit's H1 / Whitelist guidance:
# "Drop strange symbols at the boundary, don't trust filter-presence as
# a proxy for whitelisting."
ALLOWED_SYMBOLS = frozenset({
    "BTCUSDT", "ETHUSDT", "SOLUSDT", "DOGEUSDT", "XRPUSDT",
})

# BUY opens longs (or closes shorts); SELL opens shorts (or closes longs).
ALLOWED_SIDES = frozenset({"BUY", "SELL"})

# G6.4 ships MARKET only — LIMIT (IOC / GTC) is scoped out for v0.1.
ALLOWED_TYPES = frozenset({"MARKET"})


# ──────────────────────────────────────────────────────────────────────────
# Error-code → stable string mapping (audit §12)
# ──────────────────────────────────────────────────────────────────────────

# Maps Binance numeric codes to short stable identifiers. The MESSAGE
# returned by Binance is also surfaced (raw) on `error_msg`; this map is
# the structural classifier callers can match on without parsing strings.
_ERROR_CODE_TO_SLUG: dict[int, str] = {
    -1013: "invalid_message_or_lot_size",
    -1111: "precision_mismatch",
    -2010: "new_order_rejected",
    -2019: "margin_not_sufficient",
    -4061: "position_side_not_match_hedge_mode",
    -4164: "below_min_notional_exchange",
}


def _classify_error(code: int | None) -> str | None:
    """Map a Binance error code to a stable slug, or None if unknown.

    Used for logging + result tagging. Callers can also branch on the
    raw `error_code` int — the slug is for human-readable summaries.
    """
    if code is None:
        return None
    return _ERROR_CODE_TO_SLUG.get(code)


