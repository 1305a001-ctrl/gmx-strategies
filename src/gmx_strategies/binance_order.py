@dataclass(frozen=True, slots=True)
class OrderRequest:
    """Validated order request — ready to be turned into signed params.

    Construction is via the public `place_market_order` helper; the
    dataclass itself does NOT validate (since by the time we build one,
    pre-flight has already run). It's a transport.

    Fields:
      symbol            — one of ALLOWED_SYMBOLS
      side              — BUY | SELL
      type              — MARKET (only MARKET ships in G6.4)
      quantity          — base-asset units, ALREADY rounded to lot_step
      reduce_only       — when True sends `reduceOnly=true`. Audit §2: in
                          one-way mode, reduceOnly=true caps the close at
                          the existing position size — useful safety on
                          every close. NOT permitted in hedge mode (we
                          gate against hedge mode at the executor boot).
      client_order_id   — Binance `newClientOrderId`. Auto-generated if
                          not supplied. Always set in the request.
    """

    symbol: str
    side: str
    type: str
    quantity: float
    reduce_only: bool = False
    client_order_id: str | None = None

    def to_signed_params(self) -> dict[str, str | int | float]:
        """Build the param dict for `signed_post(/fapi/v1/order, ...)`.

        Binance accepts query-string-only POST; our `signed_post` puts the
        full param dict on the URL. We always set:

          - `newOrderRespType=RESULT` — synchronous response with the
            final FILLED state for MARKET. Audit §2 recommended this so
            we get `executedQty` / `cumQuote` / `avgPrice` in one round-
            trip without a separate query.

          - `recvWindow` is added downstream by `_build_signed_query`;
            do NOT add it here.

        `quantity` is sent as a number (float) — Binance accepts either
        scientific or fixed, but we already snap-rounded the value to
        `lot_step` via Decimal math in `binance_exchange_info`, so the
        float-stringification is safe within the per-symbol precision.

        `reduceOnly` is sent as the literal string `"true"` / `"false"`
        because Binance's docs explicitly say the field is STRING
        (case-insensitive). Sending a Python bool would urlencode to
        "True" / "False" — Binance's parser accepts lowercase only.

        `newClientOrderId` is REQUIRED for our idempotency pattern; the
        constructor pre-fills it if the caller didn't.
        """
        params: dict[str, str | int | float] = {
            "symbol": self.symbol,
            "side": self.side,
            "type": self.type,
            "quantity": self.quantity,
            "newOrderRespType": "RESULT",
        }
        # Binance wants the boolean as a lowercase string per docs.
        if self.reduce_only:
            params["reduceOnly"] = "true"
        if self.client_order_id is not None:
            params["newClientOrderId"] = self.client_order_id
        return params


@dataclass(frozen=True, slots=True)
class OrderResult:
    """Outcome of `place_market_order` — sim, gate-fail, or live result.

    The shape is shared across all three branches so callers can pattern-
    match on `submitted` + `gate_blocked` + `error_code` without a sum
    type.

    - `submitted=True`  → broadcast went out, Binance returned 200 with
                          the FILLED state. `order_id` + `status` +
                          `executed_qty` + `avg_price` + `cum_quote`
                          populated.

    - `submitted=False, gate_blocked=<reason>` → pre-flight or gate
                          refused. `dry_run_request` carries the exact
                          dict that WOULD have been signed. Useful for
                          dry-run debugging.

    - `submitted=False, error_code=<int>` → broadcast went out, Binance
                          returned an error (non-200 or 200 with `code`
                          field). `error_code` + `error_msg` populated.

    - `submitted=False` with NO `gate_blocked` and NO `error_code` →
                          pre-flight check itself failed locally (bad
                          side, unknown symbol, below-min-notional).
                          `error_code=-1` is the convention to mark a
                          local rejection (different from any real
                          Binance code which are all in 4-digit range).
                          `error_msg` describes the local reason.
    """

    submitted: bool
    dry_run_request: dict[str, str | int | float] | None
    order_id: int | None
    client_order_id: str | None
    status: str | None
    executed_qty: float | None
    avg_price: float | None
    cum_quote: float | None
    error_code: int | None
    error_msg: str | None
    gate_blocked: str | None
    # `error_slug` is the stable classifier from `_ERROR_CODE_TO_SLUG`
    # when `error_code` matches a known audit-§12 code; None otherwise.
    # Callers prefer matching on this over the raw int — the slug is the
    # contract; the int is the transport.
    error_slug: str | None = field(default=None)


